    public void carregarDono(){
        AnimalController controller = new AnimalController();
        Animal usu = controller.readForPk(pkAnimal);
       
        String codigo = String.valueOf(usu.getPkAnimal());
        String dono = String.valueOf(usu.getFkDono());
        txtCodigo.setText(codigo);
        txtNome.setText(usu.getNomeAni());
        txtRaca.setText(usu.getRacaAni());
        txtDono.setText(dono);
        txtEspecie.setText(usu.getEspecieAni());
        chkVivo.setSelected(usu.isVivoAni() == 1);
        chkVacina.setSelected(usu.isVacinaAni() == 1);
        lbFoto.setIcon(usu.getImagem());
        
    }
