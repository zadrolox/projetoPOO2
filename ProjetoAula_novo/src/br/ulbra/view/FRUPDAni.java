    private boolean verificarCampos(){
        if(txtNome.getText().equals("")){
            JOptionPane.showMessageDialog(null,"Campo 'Nome' em branco");
            return false;
        }
        
        if(!txtNome.getText().matches("^[\\p{L} ]+$")){
            JOptionPane.showMessageDialog(null,
                    "Campo 'Nome' possui caracteres inválidos");
            return false;
        }
        
        if(txtRaca.getText().equals("")){
            JOptionPane.showMessageDialog(null,"Campo 'Raca' em branco");
            return false;
        }
        if(txtDono.getText().matches("")){
            JOptionPane.showMessageDialog(null,
                    "Campo 'Dono' esta em branco");
            return false;
        }
        
        if(txtEspecie.getText().matches("")){
            JOptionPane.showMessageDialog(null,
                    "Campo 'Especie' esta em branco");
            return false;
        }
        
        return true;
    }
    
