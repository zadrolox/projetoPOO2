    private void btAlterarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btAlterarMouseClicked
        if(!verificarCampos()){
            return;
        }
        
        AnimalController controller = new AnimalController();
        Animal animal = new Animal();
        animal.setPkAnimal(pkAnimal);
        animal.setNomeAni(txtNome.getText());
        animal.setRacaAni(txtRaca.getText());
        animal.setFkDono(Integer.parseInt(txtDono.getText())); 
        animal.setEspecieAni(txtEspecie.getText());
        animal.setVivoAni(Utils.salvarBoolean(chkVivo.isSelected()));
        animal.setVacinaAni(Utils.salvarBoolean(chkVacina.isSelected()));
        animal.setImagem(lbFoto.getIcon());
        if(controller.alterarAnimal(animal)){
            this.dispose();
        };
    }//GEN-LAST:event_btAlterarMouseClicked

    private void btExcluirMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btExcluirMouseClicked
        int resposta = JOptionPane.showConfirmDialog(null, "Deseja excluir o animal?",
            "Confirmação", JOptionPane.YES_NO_OPTION);

        if (resposta == JOptionPane.YES_OPTION) {
            AnimalController controller = new AnimalController();
            if(controller.excluirAnimal(pkAnimal)){
                this.dispose();
            };
        }
    }//GEN-LAST:event_btExcluirMouseClicked

