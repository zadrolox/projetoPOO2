    private boolean verificarCampos(){
        if(txtNome.getText().equals("")){
            JOptionPane.showMessageDialog(null,"Campo 'Nome' em branco");
            return false;
        }
        
        if(!txtNome.getText().matches("^[\\p{L} ]+$")){
            JOptionPane.showMessageDialog(null,
                    "Campo 'Nome' possui caracteres inv√°lidos");
            return false;
        }
        
        if(txtRaca.getText().equals("")){
            JOptionPane.showMessageDialog(null,"Campo 'Ra√ßa' em branco");
            return false;
        }
        if(txtCodDon.getText().matches("")){
            JOptionPane.showMessageDialog(null,
                    "Campo 'Codigo do Dono' em branco");
            return false;
        }
        
        if(txtEspecie.getText().matches("")){
            JOptionPane.showMessageDialog(null,
                    "Campo 'Especie' em branco.");
            return false;
        }
        
        return true;
    }
    private void btSalvarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btSalvarMouseClicked
        if(!verificarCampos()){
            return;
        }
        
        AnimalController controller = new AnimalController();
        if(controller.adicionarAnimal(txtNome.getText(), txtRaca.getText(),
            Integer.parseInt(txtCodDon.getText()), txtEspecie.getText(), Utils.salvarBoolean(chkVivo.isSelected()), 
            Utils.salvarBoolean(chkVacina.isSelected()), lbFoto.getIcon())){
        this.dispose();
        };
    }//GEN-LAST:event_btSalvarMouseClicked

    private void btEscolherImagemMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btEscolherImagemMouseClicked
        JFileChooser fileChooser = new JFileChooser();
        fileChooser.setDialogTitle("Escolha um arquivo");
        
        int returnValue = fileChooser.showOpenDialog(null);

        if (returnValue == JFileChooser.APPROVE_OPTION) {
            File arquivo = fileChooser.getSelectedFile();
            Icon icon = Utils.fileParaIcon(arquivo);
            
            ImageIcon iconRedimensionado = Utils.redimensionarIcon(
                    icon, 140, 140);
            
            lbFoto.setIcon(iconRedimensionado);
        }
    }//GEN-LAST:event_btEscolherImagemMouseClicked

